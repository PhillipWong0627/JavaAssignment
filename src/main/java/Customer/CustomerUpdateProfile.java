    private void profileMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_profileMouseClicked
        DefaultTableModel model = (DefaultTableModel) profile.getModel();
        int selectedInfo = profile.getSelectedRow();

        username.setText(model.getValueAt(selectedInfo, 0).toString());
        password.setText(model.getValueAt(selectedInfo, 1).toString());
        email.setText(model.getValueAt(selectedInfo, 2).toString());
        dob.setText(model.getValueAt(selectedInfo, 3).toString());
        phonenumber.setText(model.getValueAt(selectedInfo, 4).toString());
        address.setText(model.getValueAt(selectedInfo, 5).toString());
    }//GEN-LAST:event_profileMouseClicked

